/**
 * Convert AD date to BS date
 * @param {Date} adDate
 * @returns {{ year: number, month: number, day: number, monthName: string }}
 */
export function adToBS(adDate) {
  const ad = new Date(adDate.getFullYear(), adDate.getMonth(), adDate.getDate());
  const epoch = new Date(AD_EPOCH.getFullYear(), AD_EPOCH.getMonth(), AD_EPOCH.getDate());

  let totalDays = Math.floor((ad - epoch) / (1000 * 60 * 60 * 24));

  if (totalDays < 0) return null;

  let bsYear = BS_START_YEAR;
  let bsMonth = 0;
  let bsDay = 1;

  while (true) {
    const yearData = BS_MONTH_DAYS[bsYear];
    if (!yearData) break;
    const daysInYear = yearData.reduce((a, b) => a + b, 0);
    if (totalDays < daysInYear) break;
    totalDays -= daysInYear;
    bsYear++;
  }

  const monthData = BS_MONTH_DAYS[bsYear] || [];
  for (let m = 0; m < 12; m++) {
    const daysInMonth = monthData[m] || 30;
    if (totalDays < daysInMonth) {
      bsMonth = m;
      bsDay = totalDays + 1;
      break;
    }
    totalDays -= daysInMonth;
  }

  return {
    year: bsYear,
    month: bsMonth,
    day: bsDay,
    monthName: BS_MONTH_NAMES[bsMonth],
    monthNameNp: BS_MONTH_NAMES_NP[bsMonth],
  };
}

/**
 * Format a BS date as a string
 */
export function formatBS(bsDate) {
  if (!bsDate) return "";
  return `${bsDate.day} ${bsDate.monthName} ${bsDate.year} BS`;
}
